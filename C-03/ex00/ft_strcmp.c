int		ft_strcmp(char *s1, char *s2)
{
	int vr;
	int i;

	i = 0;
	while (s1[i] != '\0' && (s1[i] == s2[i]))
	{
		i++;
	}
	vr = (s1[i] - s2[i]);
	return (vr);
}
