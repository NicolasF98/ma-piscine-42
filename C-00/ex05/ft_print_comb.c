#include <unistd.h>

void    ft_print_comb(void)
{
	int nb0;
	int nb1;
	int nb2;

	nb0 = '0';
	while (nb0 <= '9')
	{
        nb1 = nb0 + 1;
        while (nb1 <= '9')
        {
            nb2 = nb1 + 1;
            while (nb2 <= '9')
            {
                write(1, &nb0, 1);
                write(1, &nb1, 1);
                write(1, &nb2, 1);
                if (!(nb0 == '7' && nb1 == '8' && nb2 == '9'))
                    write(1, ", ", 2);
                nb2++;
            }
            nb1++;
        }
        nb0++;
    }
}
