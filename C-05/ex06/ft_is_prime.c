int		ft_is_prime(int nb)
{
	int i;
	int prime;

	prime = 0;
	i = 2;
	if (nb <= 0)
		return (0);
	while (i < nb && (nb % i != 0))
		i++;
	if (i == nb)
		return (1);
	else
		return (0);
}
