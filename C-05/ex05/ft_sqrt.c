int		ft_sqrt(int nb)
{
	int i;
	int res;

	res = 0;
	i = 1;
	while (res != nb && res < nb && nb <= 2147395600)
	{
		res = i * i;
		i++;
	}
	if (res == nb)
		return (i - 1);
	else
		return (0);
}
