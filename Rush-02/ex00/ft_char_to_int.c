int		char_to_int(char *inc)
{
	int i;
	int nb;

	i = 0;
	nb = 0;
	if ((inc[i] >= 9 && inc[i] <= 13) || (inc[i] == ' '))
		i++;
	if (inc[i] == '-')
		return (-1);
	while (inc[i])
	{
		if (inc[i] < '0' || inc[i] > '9')
			return (-1);
		nb = (nb * 10) + (inc[i] - 48);
		i++;
	}
	return (nb);
}
