int		ft_str_is_numeric(char *str)
{
	int i;
	int vr;

	if (str[0] == '\0')
		return (1);
	i = 0;
	vr = 1;
	while (str[i] != '\0' && vr == 1)
	{
		if ('0' <= str[i] && str[i] <= '9')
		{
			i++;
			vr = 1;
		}
		else
		{
			vr = 0;
		}
	}
	return (vr);
}
